import math

number = 25
square_root = math.sqrt(number)
print(f"The square root of {number} is {square_root}") 
number = 36
square_root = number ** 0.5
print(f"The square root of {number} is {square_root}")
import cmath

number = -9
square_root = cmath.sqrt(number)
print(f"The square root of {number} is {square_root}")